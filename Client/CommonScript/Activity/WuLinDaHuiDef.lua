  if not nActStartTime or nActStartTime == 0 then
    return false, "当前不是报名期"
  end
  local nSaveVal = pPlayer.GetUserValue(tbDef.SAVE_GROUP, tbDef.SAVE_KEY_TicketTime)
  if nActStartTime <= nSaveVal then
    return false, "您当前已经有门票了"
  end
  return nActStartTime <= pPlayer.GetUserValue(tbDef.SAVE_GROUP, tbDef.SAVE_KEY_CanBuyTicketTime)
end
